//This program demonstrates the while loop (to print 5 times Hello world)

public class A54_WhileLoop {
    public static void main(String [] args){
        int HW =1;

        while(HW<=5)
        {
            System.out.println("Hello World!!");
            HW ++;

        }
        System.out.println("That's All!!");
    }

    
}
