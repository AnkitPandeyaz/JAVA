//This program demonstrates the (++) Increment and (--) Decrement operators (also called postfix).

public class A52_IncrementDecrement {
    public static void main(String [] args){

        int number =5;

        //Display the value in number
        System.out.println("Number is " + number);
        System.out.println("I will Increment number.");

        //increment number

        number++;

        //Display the value in number again

        System.out.println("Now, Number is " + number);
        System.out.println("I will decrement number.");

        number--;

    }
    
}
