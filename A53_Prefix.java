//This program demonstrates the ++ and -- operators in prefiix mode.

public class A53_Prefix {
    public static void main(String[] args){

        int number= 5;

        //Display the value in number

        System.out.println("Number is " + number);
        System.out.println("T will increment number.");
        
        //increment number.
        ++number;

        //Display the value in number again

        System.out.println("Now, Nuber is " + number);
        System.out.println("I will decrement number.");
        
        //decrement number.

        --number;

        //Display the value in number once again.

        System.out.println("Now, number is " + number);



    }
    
}
